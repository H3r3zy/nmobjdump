static void dump_type(void const *const ehdr, void const * const rel,
	unsigned int type)
{
	unsigned int i = 0;
	unsigned int j = 0;
	char *t_name = malloc(18);

	for (; TYPES[i].name; i++) {
		if (MACHINE(ehdr) == TYPES[i].id)
			break;
	}
	for (; TYPES[i].name &&  ((t_message *)TYPES[i].name)[j].name; j++) {
		if ((type == SHT_REL && RELTYPE(ehdr, RELINFO(ehdr, rel)) ==
			((t_message *)TYPES[i].name)[j].id) ||
			(type == SHT_RELA && RELTYPE(ehdr, RELAINFO(ehdr, rel))
				== ((t_message *)TYPES[i].name)[j].id)) {
			printf("%-17s", strncpy(t_name,
				((t_message *)TYPES[i].name)[j].name, 17));
			return;
		}
	}
	printf("%-17s", "NONE");
}

static void dump_rela(void const *const ehdr, void const *const shdr,
	unsigned int i
)
{
	void *rel = (void *)((char *)ehdr + SHOFFSET(ehdr, shdr, i));
	void *end = (void *)((char *)rel + SHSIZE(ehdr, shdr, i));
	char *tab = (char *)ehdr + SHOFFSET(ehdr, shdr, SHLINK(ehdr, shdr, SHLINK(ehdr, shdr, i)));
	void *sym;

	for (unsigned int j = 0; rel < end; j++) {
		printf("%012lx  %012lx ", ROFFSET(ehdr, shdr, i, rel),
			RINFO(ehdr, shdr, i, rel));
		dump_type(ehdr, rel, SHT_RELA);
		sym = (char *)ehdr + SHOFFSET(ehdr, shdr, SHLINK(ehdr, shdr, i));
		sym = (char *)sym + RSIZEOF(ehdr, shdr, i) * RSYM(ehdr, RINFO(ehdr, shdr, i, rel));
		printf(" %016li ", STVALUE(ehdr, sym));
		printf(" %s", &tab[STNAME(ehdr, sym)]);
		printf(" + %i\n", RADDEND(ehdr, shdr, i, rel)); // TODO
		rel = (char *)rel + RELASIZEOF(ehdr);
	}
}

void dump_relas(void const *const ehdr, void const *const shdr)
{
	long unsigned int entries = 0;

	for (unsigned int i = 0; i < SHNUM(ehdr); i++) {
		if (SHTYPE(ehdr, shdr, i) == SHT_RELA ||
			SHTYPE(ehdr, shdr, i) == SHT_REL) {
			printf("\nRelocation section '%s' ",
				&(((char *)SHTAB(ehdr, shdr, ehdr))[SHNAME(
					ehdr, shdr, i)]));
			printf("at offset 0x%lx contains ",
				SHOFFSET(ehdr, shdr, i));
			entries = (SHTYPE(ehdr, shdr, i) == SHT_RELA ?
				(SHSIZE(ehdr, shdr, i) / RELASIZEOF(ehdr)) :
				SHSIZE(ehdr, shdr, i) / RELSIZEOF(ehdr));
			printf("%li entries:\n", entries);
			printf("  Offset          Info           Type       ");
			printf("    Sym. Value    Sym. Name + Addend\n");
			dump_rela(ehdr, shdr, i);
		}
	}
}